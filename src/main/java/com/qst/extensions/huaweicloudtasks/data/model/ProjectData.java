package com.qst.extensions.huaweicloudtasks.data.model;

public class ProjectData {
private  String project_id;
private String project_name;
private String project_num_id;

    public String getProject_id() {
        return project_id;
    }

    public void setProject_id(String project_id) {
        this.project_id = project_id;
    }

    public String getProject_name() {
        return project_name;
    }

    public void setProject_name(String project_name) {
        this.project_name = project_name;
    }

    public String getProject_num_id() {
        return project_num_id;
    }

    public void setProject_num_id(String project_num_id) {
        this.project_num_id = project_num_id;
    }
}
